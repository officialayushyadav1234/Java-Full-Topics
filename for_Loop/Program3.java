package for_Loop;

public class Program3 {

	public static void main(String[] args) {
		
		System.out.println("Program starts");
		int i;
		for(i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		System.out.println("i outside loop is: "+i);
	}
}

